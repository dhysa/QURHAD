public class HadistRiwayat_Item {
    private String hadist_nomor;
    private String nama_periwayat;
    private String isi_hadist;
    private String tema;

    public String getTema() {
        return tema;
    }

    public void setTema(String tema) {
        this.tema = tema;
    }

    public String getHadist_nomor() {
        return hadist_nomor;
    }

    public void setHadist_nomor(String hadist_nomor) {
        this.hadist_nomor = hadist_nomor;
    }

    public String getNama_periwayat() {
        return nama_periwayat;
    }

    public void setNama_periwayat(String nama_periwayat) {
        this.nama_periwayat = nama_periwayat;
    }

    public String getIsi_hadist() {
        return isi_hadist;
    }

    public void setIsi_hadist(String isi_hadist) {
        this.isi_hadist = isi_hadist;
    }



}
